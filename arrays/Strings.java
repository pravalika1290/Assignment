package com.learning.arrays;

public class Strings {
    public static void main(String[] args){
        String[] family = {"Sravan", "Pravalika", "Bob"};


        System.out.println(family[0]);
        System.out.println(family.length-1);
        int myLastName = family.length -1 ;
        System.out.println(family[myLastName]);
        System.out.println(family.length/2);
        int middleName = family.length/2;
        System.out.println(middleName);
        System.out.println(family[middleName]);


    }




}
