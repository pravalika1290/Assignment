package com.learning.arrays;

public class floating {
    public static void main(String[] args){
        float[] values = {5.55f,7.45f,9.56f,5.23f};
        System.out.println(values.length);
        System.out.println(values[0]);
        System.out.println(values.length -1);
        int myLastValue = values.length -1 ;
        System.out.println(values[myLastValue]);
        System.out.println(values.length/2);
        int midValue = values.length/2 -1;
        System.out.println(values[midValue]);




    }
}
